fruits = ["apple", "peach", "pear"]

# to print out each item from the list one by one  use for loop

for fruit in fruits:
    print(fruit)
    print(fruit + 'pie')

    #next print statement will appear after each iteration if it's inside of the loop or once if it's not

    print(fruits)
print(fruits)

